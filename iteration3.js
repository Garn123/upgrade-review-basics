let soundsCounter = array => {
    let counter = {};

    for (const iterator of array) {
        for (const key in iterator.favoritesSounds) {
            if(!counter.hasOwnProperty(key)){
                counter[key] = 1;
            }else{
                counter[key]++;
            }
        }
    }
    return counter
}

